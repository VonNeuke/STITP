function Open_Callback(hObject, eventdata, handles)
% hObject    handle to Open (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global Img_data;
[name, path] = uigetfile({'*.mat';'*.jpg';'*.png';'*.bmp'},'打开图像');
% handles.name=name;
% handles.path=path;
Img_data = load([path,name]);
MAX=max(max(max(Img_data.temp_data)));
Img_data.temp_data=Img_data.temp_data./MAX;%归一化处理
imshow(rot90(Img_data.temp_data(:,:,1)),'Parent',handles.show1);%图像旋转显示。
