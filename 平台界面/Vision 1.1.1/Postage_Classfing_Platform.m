guidata(hObject, handles);

% %显示专家图像。
A=isstrprop(name,'digit'); %提取数字
B=name(A);
C=str2num(B);
global Img_pro_data
Img_pro_data = load([path,'img',num2str(C),'_pro.mat']);
imshow(rot90(Img_pro_data.temp_data(:,:,1)),'Parent',handles.show3);

[x,y,z]=size(Img_data.temp_data);
set(handles.edit6,'string',num2str(x));%将图像的三维放入xyz中。
set(handles.edit7,'string',num2str(y));
set(handles.edit8,'string',num2str(z));
set(handles.slider1,'max',z);%设置滑条最大值

guidata(hObject,handles);



